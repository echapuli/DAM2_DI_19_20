    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        //guardamos los datos pero antes comprobamos si se cumple la validación de los datos
        if (validaDatos()){
            SimpleDateFormat formateador = new SimpleDateFormat("dd/MM/yyyy"); //formateamos la fecha
            try{
                Date fecha = formateador.parse(jTextFieldFecha.getText()); 
                IngresoGasto ingresoGasto=new IngresoGasto(fecha,jTextFieldConcepto.getText(),Float.parseFloat(jTextFieldImporte.getText()));
                LogicaNegocio.anadirIngresoGasto(ingresoGasto);
                dispose(); 
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,"Se ha producido un error al guardar los datos","Error",JOptionPane.ERROR_MESSAGE);
            }            
        }

    }//GEN-LAST:event_jButtonAceptarActionPerformed

    private void jTextFieldImporteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldImporteKeyTyped
        //Expresión regular que identifica un número entero o real
        Pattern pat = Pattern.compile("^-?[0-9]*[\\.]?[0-9]*$"); //esta expresión regular es distinta a la que evalua el numero entero ya que tiene que ir permitiendonos introducir uno a uno sus caracteres sin dar error
        Matcher real = pat.matcher(jTextFieldImporte.getText()+evt.getKeyChar()); //En el valor del textfield aun no está la tecla pulsada (por eso se la añadimos)
        if (!(real.matches()))  //si no se cumple la expresión regular anula el evento del teclado
            evt.consume();  // ignorar el evento de teclado
        //Puede ser también así:
        //if (!Pattern.matches("^-?[0-9]*[\\.]?[0-9]*$", jTextFieldImporte.getText()+evt.getKeyChar())) evt.consume();
    }//GEN-LAST:event_jTextFieldImporteKeyTyped

    private void jTextFieldConceptoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldConceptoKeyTyped
        // Vamos a hacer que el concepto no pase de 40 carácteres
        int num_caract=jTextFieldConcepto.getText().length()+1; //En el valor del textfield aun no está el caracter pulsado (por eso se suma 1)
        if (num_caract>40) { //si no se cumple la expresión regular anula el evento del teclado
            evt.consume();  // ignorar el evento de teclado
        }        
    }//GEN-LAST:event_jTextFieldConceptoKeyTyped

    private void jTextFieldFechaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldFechaKeyTyped
        //chequeamos que solo se metan los caracteres 0..9 y el /

        if (!((evt.getKeyChar()>='0'&&evt.getKeyChar()<='9')||(evt.getKeyChar()=='/'))) { 
            evt.consume();  // ignorar el evento de teclado
        }
    }//GEN-LAST:event_jTextFieldFechaKeyTyped

    private boolean validaDatos(){ //Método que valida los datos a guardar
        boolean res=true;
        String msn="";
        if ((jTextFieldFecha.getText().isEmpty())||("".equals(jTextFieldFecha.getText()))){ //en caso de que no venga el importe
            res=false;
            msn="La fecha es obligatoria \n";
        }        
        if ((jTextFieldConcepto.getText().isEmpty())||("".equals(jTextFieldConcepto.getText()))){ //en caso de que no venga el concepto
            res=false;
            msn="El concepto es obligatorio \n";
        }
        if ((jTextFieldImporte.getText().isEmpty())||("".equals(jTextFieldImporte.getText()))){ //en caso de que no venga el concepto
            res=false;
            msn=msn+"El importe es obligatorio \n";
        }
        if (!Pattern.matches("^([0][1-9]|[12][0-9]|3[01])((/|-)([0][1-9]|[1][0-2]))((\\/|-)(\\d{4}))$", jTextFieldFecha.getText())){
            res=false;
            msn=msn+"No es una fecha válida dd/mm/aaaa \n";
        }
        if (!Pattern.matches("^-?[0-9]*([\\.]?[0-9]+)?$", jTextFieldImporte.getText())){ //puede ser un número con signo - o no, y llevar o no decimales
            res=false;
            msn=msn+"No es un formato de importe correcto \n";
        }
        if (!res){
            JOptionPane.showMessageDialog(this,msn,"Error",JOptionPane.ERROR_MESSAGE);
        }
        return res;
    }
    
